<?php

$path = parse_url($_SERVER["REQUEST_URI"], PHP_URL_PATH);

$file = __DIR__ . $path;

/*
|--------------------------------------------------------------------------
| Servir archivos estáticos
|--------------------------------------------------------------------------
*/

if ($path !== "/" && file_exists($file)) {
    return false;
}

/*
|--------------------------------------------------------------------------
| Rutas dinámicas
|--------------------------------------------------------------------------
*/

$route = trim($path, "/");

if ($route !== "" && preg_match("/^[-a-zA-Z0-9]+$/", $route)) {
    $_GET["ruta"] = $route;
}

/*
|--------------------------------------------------------------------------
| Cargar aplicación
|--------------------------------------------------------------------------
*/

require __DIR__ . "/index.php";